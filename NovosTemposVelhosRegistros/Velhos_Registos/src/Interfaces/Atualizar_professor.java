    private void Btn_salvarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_Btn_salvarActionPerformed

        String cod = Text_cod.getText(); 
        String nome = Text_nome.getText();
        String senha = Text_senha.getText();
        String atividade = Text_atividade.getText();
        
        Atualizar atualizaProf = new Atualizar();
        try {
            atualizaProf.Atualizar_Professor(
                    Integer.parseInt(cod),
                    Integer.parseInt(senha),
                    nome,
                    atividade
            );
            JOptionPane.showMessageDialog(this, "Dados atualizados com sucesso!",
                    "Atualiza√ß√£o efetuada", JOptionPane.INFORMATION_MESSAGE);
        } catch (SQLException ex) {
            JOptionPane.showMessageDialog(this, "Erro ao atualizar ;p");
        }catch(RuntimeException ex){
            JOptionPane.showMessageDialog(this, "Preencha todos os campos corretamente.",
            "Erro", JOptionPane.ERROR);
        }
        this.dispose();
    }//GEN-LAST:event_Btn_salvarActionPerformed

